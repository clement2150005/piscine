void	rush(int x, int y);

int	main(int argc, char *argv[])
{
	long long	x;
	long long	y;

	x = 0;
	y = 0;
	if (argc != 3)
		return (0);
	while (*argv[1])
	{
		x *= 10;
		x += *argv[1] - '0';
		argv[1]++;
	}
	while (*argv[2])
	{
		y *= 10;
		y += *argv[2] - '0';
		argv[2]++;
	}
	if (((1 <= x) && (x <= 2147483647)) && ((1 <= y) && (y <= 2147483647)))
		rush(x, y);
	return (0);
}
